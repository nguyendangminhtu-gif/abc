package org.example.demosof3041.buoi1;

public class Caculator {
    public int add(int a, int b) {
        return a + b;
    }
    public int tru(int a, int b) {
        return a - b;
    }
    public int nhan(int a, int b) {
        return a * b;
    }
    public int chia(int a, int b) {
        return a / b;
    }
}
